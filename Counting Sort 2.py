def countingSort(arr):
    # Write your code here
    maximum=max(arr)
    count=[0]*(maximum+1)
    
    for i in arr:
        count[i]+=1
        
    target=0
    for i,v in enumerate(count):
        for j in range(v):
            arr[target]=i
            target+=1
    return arr        

